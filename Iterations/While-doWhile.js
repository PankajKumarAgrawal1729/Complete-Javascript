// while

let index = 0;
while (index <= 10) {
    console.log(`Value of index is ${index}`);
    index += 2;
}

let myArray = ["val1", "val2", "val3"];
let i = 0;
while (i < myArray.length) {
    console.log(`Value is ${myArray[i]}`);
    i++;
}

// do-while

let score = 11;
do{
    console.log(`Score is ${score}`);
    score++;
}while(score <= 10)